<div class="panel-body">
  <div class="tab-content">
   <div class="tab-pane fade in active" id="tab1default">
    <p class="f-bold">Como é realizado o processo de aquisição de materiais na Biblioteca?</p>

    <p>Mediante a indicação dos professores é elaborado um ementário contendo as referências bibliográficas de cada disciplina, estas são avaliadas pelos coordenadores de cada curso, tendo em vista atender às necessidades dos usuários.</p>

  </div>
  <div class="tab-pane fade" id="tab2default">
    <p class="f-bold">Posso devolver os materiais e deixar a multa pendente?</p>

    <p><b>Sim</b>, materiais com atrasos podem ser devolvidos independentemente do pagamento da multa correspondente. Lembrando que no pagamento que fores pagar, o valor será o mesmo do dia da devolução efetiva do material, sem acréscimos, independentemente de quando foi devolvido. Usuários com itens em atraso e/ou multas pendentes em seu nome tem seu cadastro bloqueado para empréstimos, renovações e reservas até a regularização da pendência.</p>

     <p class="f-bold">Outra pessoa pode fazer a devolução do material em atraso?</p>

    <p><b>Sim</b>, outra pessoa pode fazer a devolução do material na  biblioteca em que foi emprestado, deixando o valor da multa pago ou pendente.</p>
  </div>

  <div class="tab-pane fade" id="tab3default">
    <p class="f-bold">Não sou aluno/professor da FID, posso utilizar o serviço de empréstimo da Biblioteca?</p>

    <p><b>Não</b>. A biblioteca da FID atende a comunidade acadêmica. Para utilizar o serviço de empréstimo é necessário possuir algum vínculo com a Instituição.</p>

    <p class="f-bold">Quem pode utilizar o serviço de empréstimos de material bibliográfico na FID?</p>

    <p>Usuários regulares, isto é, Professores , Estudantes de Graduação e Pós-Graduação, Extensão e Funcionários técnico-administrativos da FIDQuantos materiais podem ser emprestados para os usuários e por quanto tempo?Alunos de graduação3 livros por 8 dias – podendo ser renovado, caso não haja reserva e 1 recurso individual  por 3 dias.Alunos de Pós-graduação4 livros por 15 dias – podendo ser renovado, caso não haja reserva e 1 recurso individuais por 3 dias.Professores4 livros por 15 dias – podendo ser renovado, caso não haja reserva e 2 recursos individuais por 5 dias.Técnicos Administrativos2 livros por 8 dias – podendo ser renovado, caso não haja reserva e 1 recurso individual por 3 dias.Os materiais como periódicos, obras de referências (dicionários, enciclopédias), somente são emprestados para fotocópias (quando permitida a reprodução) devendo ser devolvidos em seguida.</p>
  </div>

  <div class="tab-pane fade" id="tab4default">
    <p class="f-bold">Qual o valor da multa?</p>

    <p>O valor da multa é de R$ 0,50 por dia para cada livro. Para que a multa não continue aumentando, dirija-se a biblioteca e, pague a multa e renove a seus empréstimos. No site da FID , você tem acesso aos horários de funcionamento da  Biblioteca. No momento do empréstimo, todo usuário toma ciência da data de devolução da obra, sendo ele o responsável pela devolução dentro do prazo estipulado.</p>

    <p class="f-bold">A multa pode ser paga por outra pessoa?</p>

    <p><b>Sim</b>. Para isto basta que a pessoa devolver o livro e pagar a multa.</p>

    <p class="f-bold">A multa é cobrada também nos finais de semana e feriados?</p>

    <p><b>Não</b>. a multa é cobrada por dias utéis.</p>

    <p class="f-bold">Em caso de falta de energia ou de problemas no Sistema, onde não foi possível a renovação, paga-se a multa do dia, caso seja a data de devolução de livros?</p>

    <p><b>Não</b>, a multa do dia não é cobrada. A data de devolução de todos os materiais que expiravam no dia são transferidas para o dia seguinte.</p>
  </div>

  <div class="tab-pane fade in active" id="tab5default">
    <p class="f-bold">Perdi um livro que estava em meu nome, como proceder nesse caso? E se o livro estiver danificado?</p>

    <p>No caso de perda, roubo ou danos, o usuário deverá comunicar imediatamente a Biblioteca para evitar a cobrança da taxa por atraso. Ele terá o prazo de trinta dias para ressarcir a Biblioteca com um exemplar idêntico (mesmo título, autor, editora e edição) ou de edição mais atualizada da obra perdida. Caso a perda tenha sido comunicada após o vencimento do empréstimo, além da reposição da obra, o usuário deverá pagar a multa referente aos dias em atraso. Em caso de obra esgotada o bibliotecário responsável estabelecerá o valor da indenização compatível com o valor estimado da obra.</p>

  </div>

