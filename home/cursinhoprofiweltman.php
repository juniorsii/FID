						<p>O Professor Francisco Iweltman Vasconcelos Mendes (1964-2013) nasceu em Sobral-CE. Licenciou-se em Estudos Sociais pela UVA (1987), onde também bacharelou-se em História (1988); Ainda no Ceará tornou-se Especialista em Metodologia do Ensino Superior pela UFC (1990). Dando prosseguimento a sua formação, tornou-se Mestre em Educação pela UFPI (1999) e Doutor em Educação pela UDE (Montevideo UY-2011 – UFPI-2012).</p>
						<p>O Professor Iweltman Mendes foi professor da UFPI, C.M.R.V. Parnaíba, Faculdade Piauiense (FAP) e Faculdade Internacional do Delta (FID). Foi membro da Academia Parnaibana de Letras (APAL) e Sócio Fundador do Instituto Histórico, Geográfico e Genealógico de Parnaíba (IHGGP). Na gestão pública foi Secretário Municipal de Administração e Planejamento de Sobral (CE); Secretário de Educação de Parnaíba (PI); Vereador de Parnaíba (2005-2008) e Superintendente Estadual de Ciência e Tecnologia.</p>
