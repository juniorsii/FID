                        <p>A Comissão Própria de Avaliação (CPA) da FID foi criada em conformidade com as determinações legais que estruturam a sistemática de avaliação do Ensino Superior no Brasil: Inciso IX, do Artigo 9º, da Lei nº 9.394/96, que institui as Diretrizes e Bases da Educação Nacional; Artigo 1º, da Lei nº 10.861/04, que institui o Sistema Nacional de Avaliação da Educação Superior – SINAES; Portaria nº 2.051/04, que regulamenta os procedimentos do SINAES; Resolução nº 01/05 do CONAES, que estabelece prazos para os procedimentos da Avaliação Institucional.</p> 

                        <p>A CPA da FID, cujo regulamento foi aprovado em junho de 2006, é constituída por representantes do corpo discente e docente, funcionário técnico-administrativo da Faculdade e representante da sociedade civil. Com a instalação de um sistema de informação e divulgação de dados mais ágil e preciso, o estudante responde, no ato da matrícula, ao Questionário de Autoavaliação Institucional eletrônico disponível na plataforma do Sistema Acadêmico cujo preenchimento é completamente anônimo.</p>

                        <p>As informações coletadas são enviadas para o banco de dados e transformadas em gráficos e relatórios. Porém, não apenas os acadêmicos, mas os docentes, coordenadores de curso e corpo técnico-administrativo da FID também preenchem os referidos questionários e, em breve, serão disponibilizados para os membros da comunidade externa.</p>

                        <p>Esses questionários constituem, pois, uma ferramenta fundamental para o planejamento administrativo-pedagógico uma vez que revelam as potencialidades e dificuldades dessa Instituição em busca da melhoria da qualidade da formação, da produção do conhecimento e da extensão. Percebe-se, dessa forma, a importância da participação efetiva da Comunidade Acadêmica nesse Processo de Autoavaliação Institucional a fim de que as respostas desses questionários possam fornecer subsídios consistentes para o planejamento de ações e implementação de melhorias que beneficiem toda a comunidade acadêmica.</p><br><br>


