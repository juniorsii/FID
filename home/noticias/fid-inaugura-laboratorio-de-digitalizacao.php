                <div class="detail-description textalign">
                    <span class="dropcaps">F</span>oi inaugurado na noite desta quinta-feira, dia 26/04/2018, na Faculdade Internacional do Delta, em Parnaíba – PI, o Laboratório de Digitalização e Núcleo de Organização de Documentos – LADINO.

                </div>

                <div class="detail-content textalign">
                    <p>
                        O LADINO se propõe a promover a preservação e a disponibilização para a comunidade em geral das pesquisas e das fontes documentais utilizadas pelo grupo de pesquisadores. O interesse é manter a discussão já existente no âmbito da FID, especialmente nos programas de pós-graduação voltados para a investigação histórica e que fazem amplo manuseio de fontes manuscritas e impressas. Através da institucionalização do LADINO pretende-se catalogar e digitalizar um conjunto de documentos produzidos por instituições do judiciário, parlamentares, cartoriais, policiais, médico-sanitaristas e eclesiásticas. 
                    </p><br><br>

                    <blockquote class="detail-quote">
                        <div class='quotemarks'>
                            O objetivo deste espaço é promover o conhecimento a respeito da historiografia piauiense...<br><br>
                            Congregando, para isso, um número considerável de docentes, pesquisadores e discentes. Na ocasião foram disponibilizadas todas as edições impressas do Jornal O Piaguí em seus 10 anos de existência. 
                        </div>
                        <small>@2018. Prof. Dr. Naudiney C. G. <a href="#">Mais detalhes</a></small>

                    </blockquote><br><br>

                     <p>
                        O LADINO tem como principal meta desenvolver projetos de pesquisas de caráter acadêmico-científico voltados para a compreensão da realidade brasileira, em geral, e piauiense, em nível específico. Nesse sentido, o LADINO terá como horizonte de pesquisa a formação da sociedade brasileira entre os séculos XVIII e XXI e como eixos temáticos das suas pesquisas os seguintes tópicos: História da Saúde e da Doença, Instituições Políticas, Militares e Jurídicas, Escravidão e horizontes da liberdade: negros e índios e relações familiares, História da Imprensa, as identidades de gênero e as práticas discursivas católicas.
                    </p><br><br><br><br>

                </div>

