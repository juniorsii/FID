                <div class="detail-description textalign">
                    <span class="dropcaps">O</span> Ministério da Educação divulgou nesta sexta-feira, 25, novas datas referentes aos processos seletivos do primeiro semestre de 2019 do Sistema de Seleção Unificada (Sisu), do Programa Universidade para Todos (ProUni), do Fundo de Financiamento Estudantil (Fies) e do Programa de Financiamento Estudantil (P-Fies). O novo calendário foi publicado no Diário Oficial da União.

                </div>

                <div class="detail-content textalign">
                    <p>
                        <b>Sisu</b> – O Sisu, cujas inscrições já estão abertas e que já havia tido o prazo final prorrogado para o próximo domingo, 27, também teve mudança no que diz respeito à lista de espera. O novo período para que os candidatos manifestem o interesse em participar da lista de espera é de zero hora de 29 de janeiro às 23h59 de 5 de fevereiro, observando o horário oficial de Brasília. A partir desta edição, os candidatos selecionados em chamada regular não poderão mais participar da lista de espera. 
                    </p>
                    <p>
                        <b>ProUni</b> – As inscrições para o processo seletivo do ProUni, diante das mudanças anunciadas no Sisu, serão realizadas de zero hora de 31 de janeiro até as 23h59 de 3 de fevereiro, pelo horário oficial de Brasília. Os resultados dos candidatos pré-selecionados em primeira chamada estarão disponíveis na página do programa a partir do dia 6 de fevereiro. Já a lista com os nomes dos selecionados em segunda chamada será divulgada no dia 20 de fevereiro. 
                    </p>
                    <p><br><br>

                    <blockquote class="detail-quote">
                        <div class='quotemarks'>
                            Os candidatos pré-selecionados no ProUni em primeira chamada deverão comparecer à instituição de ensino superior escolhida para entrega de documentação que comprove as informações prestadas no período de 6 a 14 de fevereiro. O prazo para quem for pré-selecionado em segunda chamada vai de 20 a 27 de fevereiro. 
                        </div>
                        

                    </blockquote><br>

                    <iframe width="740" height="500" src="https://www.youtube.com/embed/kAY0txUAYPc" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>

                    <p>
                        <b>Fies</b> – De acordo com o novo calendário divulgado nesta sexta-feira, 25, as inscrições para o processo seletivo do Fies, referente ao primeiro semestre de 2019, ocorrerão de zero hora de 7 de fevereiro às 23h59 de 14 de fevereiro, sempre de acordo com o horário oficial de Brasília. O resultado da pré-seleção para as modalidades Fies e P-Fies será divulgado no dia 25 de fevereiro.
                    </p>
                    <p>
                        Os candidatos pré-selecionados na modalidade Fies deverão complementar e concluir sua inscrição no sistema FiesSeleção no período de 26 de fevereiro a 7 de março. A convocação dos candidatos participantes da lista de espera ocorrerá de 27 de fevereiro a 10 de abril.
                    </p>
                      <p>
                        Os candidatos que tiveram a inscrição do primeiro e segundo semestres de 2018 postergadas deverão concluir a inscrição no sistema FiesSeleção entre zero hora de 28 de fevereiro e 23h59 de 11 de março, observando o horário oficial de Brasília.
                    </p>
