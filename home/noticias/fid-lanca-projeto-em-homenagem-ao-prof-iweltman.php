                <div class="detail-description textalign">
                    <span class="dropcaps">O</span> Cursinho Popular Professor Iweltman Mendes gestado e gerido pela Faculdade Internacional do Delta (FID) nasce com uma missão, educar os agentes transformadores do amanhã.

                </div>

                <div class="detail-content textalign">
                    <p>
                        O cursinho popular visa permitir o acesso da população ao ensino superior, ofertando um Preparatório para o Exame Nacional do Ensino Médio (ENEM). O Cursinho Popular está inserido dentro do tripé Ensino, Pesquisa e Extensão, cumprindo, a partir da extensão universitária, o papel de transformar a realidade social na medida em que leva à comunidade atividades de natureza cientifica, política e cultural. 
                    </p>
                    <p>
                        O Cursinho Popular presta uma homenagem ao Professor Iweltman Mendes, fazendo-lhe um agradecimento póstumo pela sua larga contribuição na Educação na cidade de Parnaíba, em especial na Faculdade Internacional do Delta, instituição nascida do seu sonho e da sua luta.
                    </p><br><br>

                    <blockquote class="detail-quote">
                        <div class='quotemarks'>
                            O Professor Iweltman Mendes foi autor também de dezesseis livros<br><br>
                             Entre eles “Parnaíba: Educação e Sociedade: Da colonização ao fim do Estado Novo”, “Parnaíba: Educação e Sociedade. Da colonização à primeira república”, “Associação comercial de Parnaíba: Lutas e conquistas”, “História da Educação Piauiense”, etc. 
                        </div>
                        <small>@2018. Prof. Dr. Naudiney C. G. <a href="#">Mais detalhes</a></small>

                    </blockquote><br><br>

                    <p>
                        O Professor Francisco Iweltman Vasconcelos Mendes (1964-2013) nasceu em Sobral-CE. Licenciou-se em Estudos Sociais pela UVA (1987), onde também bacharelou-se em História (1988); Ainda no Ceará tornou-se Especialista em Metodologia do Ensino Superior pela UFC (1990). Dando prosseguimento a sua formação, tornou-se Mestre em Educação pela UFPI (1999) e Doutor em Educação pela UDE (Montevideo UY-2011 – UFPI-2012).
                    </p>
                     <p>
                        O Professor Iweltman Mendes foi professor da UFPI, C.M.R.V. Parnaíba, Faculdade Piauiense (FAP) e Faculdade Internacional do Delta (FID). Foi membro da Academia Parnaibana de Letras (APAL) e Sócio Fundador do Instituto Histórico, Geográfico e Genealógico de Parnaíba (IHGGP). Na gestão pública foi Secretário Municipal de Administração e Planejamento de Sobral (CE); Secretário de Educação de Parnaíba (PI); Vereador de Parnaíba (2005-2008) e Superintendente Estadual de Ciência e Tecnologia.
                    </p><br><br><br><br>

