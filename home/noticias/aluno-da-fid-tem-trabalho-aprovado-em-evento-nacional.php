                    <p>
                        Evento que ocorrerá entre os dias 17 e 21 de setembro de 2018, no campus do Centro de Formação de Professores (Cajazeiras – Paraíba), na Universidade Federal de Campina Grande (CFP/UFCG).
                        Em sua décima edição, a Semana Nacional de História propiciará aos participantes a oportunidade de refletir sobre o tema O fazer/ensinar História(s): possibilidades e desafios da diversidade cultural. 
                    </p>
                    <p>
                        Este evento tem como objetivo principal reunir pesquisadores, docentes, discentes, ativistas e militantes para dialogar os resultados de seus estudos e suas experiências cotidianas em torno das diversas possibilidades no ensino de História e das variadas formas na construção da educação na contemporaneidade. Pela segunda vez, o discente do curso de História teve seu trabalho aprovado e viajará para a Paraíba, para efetuar a defesa de sua produção intelectual em meio a outros intelectuais de nosso cenário nacional.
                    </p>

